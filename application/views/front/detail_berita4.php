            Generasi Millenial Pengaruhi Nasib Ekonomi Indonesia
          </h3><br>
            <p style="color: #002147;">
            <p class="card-text"><small class="text-muted">Dewi Widya Ningrum</small>
            <p>
                Perkembangan teknologi telah membawa banyak perubahan pada gaya hidup anak muda yang lahir di tahun 1980-an hingga 2000-an, yang biasa disebut generasi millenial atau generasi digital native. 
                Mereka tumbuh dalam lingkungan serba digital. Berkat internet, mereka dapat menjalankan berbagai aktivitas menjadi lebih mudah.Generasi millenial menemukan caranya sendiri untuk terhubung dan terkoneksi dengan orang lain lewat sosial media, seperti Twitter, Facebook, dan Path. Tak ada lagi jarak, semua saling terkoneksi. 
                Mereka mengubah tatanan nilai dan gaya hidup selama ini menjadi serba digital. Pada 2020, jumlah generasi millenial diperkirakan menjadi yang terbesar di Indonesia. Karena itu, Yoris Sebastian dari OMG Consulting menilai perkembangan ekonomi di Indonesia akan sangat dipengaruhi oleh generasi tersebut.</p>
            <br>

            <p>
                "Mereka menentukan mau dibawa ke mana arah perekonomian kita pada lima tahun mendatang. Untuk itu, kita harus benar-benar mendalami karakter dan gaya hidup mereka, agar tidak menjadi malapetaka bagi kita,
                " kata Yoris dalam keterangan tertulis. Sementara itu, menurut survei kumpulan data obrolan di Twitter yang dilakukan Provetic pada 1 Desember-31 Januari 2016, sebanyak 45 persen user terbesar berada pada rentang 20â€“24 tahun, 
                dari total responden sebanyak 4.670 akun. Dari survei ini, karakter unik lainnya terlihat skala prioritas dari keinginan mereka. Selain memasukkan "memiliki rumah" dan "bisnis" di dalam Top Wish List; perilaku konsumtif untuk belanja, traveling, membeli tiket konser, dan film juga menjadi prioritas utama mereka.
            </p> 
            <br>

            <p>
                Dengan adaptasi teknologi, ide kreatif dan orisinil untuk mengakomodir semua aktivitas mereka jadi lebih mudah, muncul berbagai inovasi gaya hidup digital yang revolusioner.Adaptasi digital yang tinggi ini telah membawa perubahan gaya hidup digital dalam melaksanakan rutinitas sehari-hari, 
                dari cara berkomunikasi, interaksi melalui jejaring sosial,
                transaksi pembayaran hingga belanja kebutuhan sehari-hari.Dari sini tercipta ekosistem digital yang membentuk masyarakat millenial di Indonesia. Salah satu indikatornya adalah munculnya sejumlah startup di Indonesia.  </p>
            <br>

            <p>
                William Tanuwijaja, punggawa Tokopedia, menggerakkan perubahan di e-Commerce. Dimulai dengan mendapatkan investasi sebesar us$ 100 juta dari Softbank dan Sequoia, perusahaan investasi yang telah mendukung Apple, Google, Whatsapp, dll, 
                William memimpin pasukan millennial di Tokopedia untuk terus menciptakan nilai tambah bagi para seller dan pelanggan Tokopedia. "Dengan perkembangan teknologi digital yang semakin canggih, millenial ikut membangun ekonomi lewat dunia digital. Tumbuhnya ekosistem digital ikut membangun kekuatan ekonomi baru, ujar Iwan Setyawan, CEO Provetic.
            </p>
            <br>

            <p>
                Yes Boss, sebuah digital apps yang dapat membantu seluruh kebutuhan masyarakat, berhasil membuat Ahmad Rizqi Meydiarso melepaskan kehidupan yang nyaman di Jerman bekerja di Airbus. Ahmad Rizqi memutuskan untuk pulang ke Tanah Air agar dapat turut membangun kekuatan ekonomi Indonesia. 
                Tidak hanya di perekonomian, banyak perubahan yang dibawa millennial menggunakan platform digital. Kitabisa.com yang digagas oleh Alfatih Timur bersama Vikra Ijas membawa perubahan di ranah sosial. Peristiwa pembakaran Masjid Tolikara pada saat shalat berjamaah Idul Fitri 2015 menimbulkan keprihatinan di berbagai lapisan masyarakat. 
                Berkat platform crowdfunding Kitabisa.com ini, Pandji Pragiwaksono berhasil mengumpulkan dana untuk membangun kembali Masjid sebanyak Rp 300 juta hanya dalam waktu 3 hari.
            </p>
            <br>

            <p>
                Kehadiran startup mengikuti perubahan gaya hidup generasi millenial. Pertumbuhan millenial membuat populasi mereka menjadi sebuah kekuatan ekonomi baru. Ke depannya, mereka lah penggerak roda perekonomian Indonesia.
                Pemerintah, melalui Badan Ekonomi Kreatif juga telah mendukung langkah generasi millenial lewat peta jalan e-Commerce. Penetapan ini menjadi program nasional yang diluncurkan akhir Januari 2016.             
                Menurut data Departemen Perdagangan RI, pada tahun 2016, nilai bisnis e-Commerce di Indonesia diperkirakan bisa mencapai Rp 120 triliun, dan bisa mencapai Rp 140 triliun dan dalam tiga tahun ke depan.
            </p>
            
